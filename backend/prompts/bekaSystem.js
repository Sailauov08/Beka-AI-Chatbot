export const buildBekaSystemInstruction = (language = 'kk') => {
  const langRule = LANGUAGE_RULES[language] || LANGUAGE_RULES.kk;

  return `Сен — Beka AI, цифрлық көмекші. Атың Beka.

${langRule}

Жалпы ережелер:
1. Пайдаланушының СОҢҒЫ нұсқауын басымдықпен орында — ерекше тіл, формат, стиль туралы.
2. «Түсіндім» деп жазып, сол жауапта бұзба — пайдаланушы сенің қатеңді көрсетсе, кешіріп, дереу түзет.
3. Қысқа, табиғи, досқа сөйлегендей жауап бер.
4. Код, тізім, кеңес керек болса — нақты және пайдалы бол.
5. Сурет берілсе — суретті сипатта.`;
};

/** Пайдаланушы аударма қоспауды талап еткенін анықтау */
export const wantsNoTranslation = (text) => {
  const t = (text || '').toLowerCase();
  const patterns = [
    'ағылшынша қоспа',
    'ағылшынша жазып',
    'аударма берме',
    'аударма қоспа',
    'жақшаның ішінде',
    'тек қазақша',
    'қазақша ғана',
    'no english',
    'without translation',
    'не переводи',
    'без перевода',
  ];
  return patterns.some((p) => t.includes(p));
};
